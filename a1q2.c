#include <stdio.h> // Preprocessor directive : Used to import different libraries.

int main() { // Main function: Program execution starts from here.
    int num; // Variable named num which can store data of integer type.
    scanf("%d",&num); // Scan function: Used to take user input.
    printf("%s", "Debadarshi Omkar"); // Print function: Used to display output. "%s" is a format specifier which represents the type of data.
    return 0; // return statement: At the end of the every function return statement returns the answer accordingly.
}